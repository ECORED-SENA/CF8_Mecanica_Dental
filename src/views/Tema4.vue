              h3.clr_2 Titanio
          p.mb-5 Ti es un metal de transición y un elemento con número atómico 22. El Ti tiene un acabado brillante y se caracteriza por su color plateado, baja densidad y alta resistencia. Tiene una alta capacidad para resistir la corrosión en diversos medios, como agua de mar, agua regia y cloro. También se afirma que Ti es biocompatible, ya que no es tóxico, ni es rechazado por el cuerpo humano. Por lo tanto, el Ti y sus aleaciones se pueden utilizar en varios usos médicos, por ejemplo, implantes e implementos quirúrgicos, y en odontología, por ejemplo, pilares, prótesis y alambres de ortodoncia. El titanio se usa a menudo como metal puro o en forma de aleación en aplicaciones aeroespaciales y en trabajos médicos y dentales. Suele estar aleado con otros metales como el vanadio (V) y el aluminio (Al). Forma entonces aleaciones ligeras, pero al mismo tiempo fuertes para la fabricación de implantes orales o estructuras. 

